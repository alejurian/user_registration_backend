    public function update(Request $request, $id)
    {
        $validator = Validator::make(['id'=>$id], [
            'id' => 'required|string|exists:verifications,token',
        ]);

        if ($validator->fails()) {
            return $validator->errors();
        }

        $verification = Verification::where('token', $id)->with('user')->first();

        if ($verification->user->verification_status == 'verified') {
            return 'El usuario ya estÃ¡ verificado';
        }
        $verification->user->verification_status = 'verified';
        $verification->user->save();
        
        return $verification;
    }

